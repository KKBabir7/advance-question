function requestLocationData() {
  // Simulate request for location data using IP geolocation API
  // var token = '5adbe8c9d9628e'; // Replace with your actual token

  $.ajax({
    url: 'https://api.bigdatacloud.net/data/reverse-geocode-client',
    // data: {
    //   'token': token
    // },
    dataType: 'json',
    success: function(data) {
      if (data.countryCode in country_data) {
        var countryCode = data.countryCode;
        var countrySelect0 = $('.eventsCalCountry0');
        var timezoneSelect0 = $('.eventsCalTimezone0');
        var countrySelect = $('.eventsCalCountry');
        var timezoneSelect = $('.eventsCalTimezone');
        var phoneCodeSelect = $('#phoneCode');

        // Update independent pair
        countrySelect0.val(countryCode).trigger('change.select2');
        var timezones0 = country_data[countryCode].timezones;
        if (timezones0 && timezones0.length > 0) {
          timezoneSelect0.val(timezones0[0].tzid).trigger('change.select2');
        }

        // Update other pairs
        countrySelect.val(countryCode).trigger('change.select2');
        var timezones = country_data[countryCode].timezones;
        if (timezones && timezones.length > 0) {
          timezoneSelect.val(timezones[0].tzid).trigger('change.select2');
        }
        phoneCodeSelect.val(countryCode).trigger('change.select2');
      }
    },
    error: function(jqXHR, textStatus, errorThrown) {
      console.error('Error retrieving location data:', errorThrown);
    }
  });
}

